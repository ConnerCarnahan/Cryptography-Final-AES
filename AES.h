/****************************** TYPES ******************************/

typedef struct {
	uint32_t digest[AES_DIGEST_SIZE];
  uint8_t round;
} AES_state;

/*********************** FUNCTION DECLARATIONS **********************/

//Helper functions
uint8_t AES_multCoef(uint8_t a, uint8_t b); // Multiplies two elements of GF(2^8)
uint32_t AES_multColumn(uint32_t a, uint32_t b); // Multiplies two 32 bit numbers in GF((2^8)^4)
void AES_xtimes(uint8_t a, uint8_t* b); // Multiplies a byte by x
uint8_t AES_getByte(uint32_t a, uint8_t n); // Gets the (n+1)th byte in word a and shifts it into an 8 bit number
uint32_t AES_SubColumn(uint32_t col, const uint8_t* table); // This is mostly for key expansion, it does SubBytes but only on one 32 bit number

//Main Functions:
void AES_KeyExpansion(uint32_t* key, uint32_t* w, uint8_t Nk, uint8_t Nr); //Creates the Key Register for Ciphering
void AES_DW(uint32_t* w, uint8_t Nr); //Converts the Key Register into the Deciphering Key Register
void AES_AddRoundKey(AES_state* state, uint32_t* w); // XORs the state digest with the key register for that round
void AES_MixColumns(AES_state* state, uint32_t ax); // Multiplies the columns of the digest by a polynomial ax
void AES_ShiftRows(AES_state* state, uint8_t shiftnum); // Shifts rows in the state, determined by shiftnum
void AES_SubBytes(AES_state* state, const uint8_t* table); // Substitutes bytes in the table into the cells of the state
void AES_Cipher(uint32_t* key, AES_state* state, uint32_t ax, const uint8_t* table, uint8_t shiftnum, uint8_t Nr, uint32_t* w); // Full ciphering Protocol

//Testing functions:
void PrintState(AES_state* state); // Prints the current state register
void PrintRegister(uint32_t* w,uint8_t round); // Prints the key register at a round
void LoadKey(uint32_t* w, char* s); // Loads in a string to a register
void AES_PrintOutput(uint32_t* digest, uint8_t size);

